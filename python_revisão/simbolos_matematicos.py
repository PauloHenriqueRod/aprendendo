# / = divisão, // = divisão inteira, * = multiplicação
# ** = potência, % == resto da divisão

print(f'23/4={23/4}')
print(f'23//4={23//4}')
print(f'23*4={23*4}')
print(f'23**4={23**4}')
print(f'23%4={23%4}')
print('='*30)

# format(valor, [=, b, o, x, d, E, X])
# = -> COLOCA O SINAL DE IGUAL MAIS À ESQUERDA
# b -> CONVERTE O VALOR EM BINÁRIO 
# o -> TRANSFORMA PARA O FORMATO OCTAL
# x -> CONVERTO PARA O VALOR HEXADECIMAL
# E -> FORMATO CIENTÍFICO
# X -> FORMATO HEXADECIMAL COM LETRAS MAIÚSCULAS

print(format(27, 'b'))
print(format(27, 'o'))
print(format(27, 'x'))
print(format(27, 'E'))
print(format(27, 'X'))
